#include "../../inc/header.h"

void		execute_tokens(t_listc **tok, t_sh *sh)
{
	t_listc	*cp;

	if ((cp = (*tok)))
	{
		if (sh)
			;
		while (cp)
		{
			cp = cp->next;
		}
	}
}

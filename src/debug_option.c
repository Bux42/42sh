#include "../inc/header.h"

void		check_debug(char **av, t_sh *sh)
{
	sh->debug = 0;
	if (av[1])
		if (ft_strcmp(av[1], "-debug") == 0)
			sh->debug = (sh->debug | DEBUG);
}
